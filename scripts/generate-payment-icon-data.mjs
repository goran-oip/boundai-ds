/**
 * Dev: builds payment mark metadata for bd-payment-method-icon from Iconify + Figma SVGs.
 * Run: node scripts/generate-payment-icon-data.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')

function loadPack(name) {
  const p = join(root, `node_modules/@iconify-json/${name}/icons.json`)
  return JSON.parse(readFileSync(p, 'utf8')).icons
}

/** Iconify dimension defaults — see https://iconify.design/docs/types/iconify-json.html */
function viewBoxFor(pack, data) {
  if (data.width && data.height) return `0 0 ${data.width} ${data.height}`
  if (data.height && !data.width) return `0 0 256 ${data.height}`
  if (data.width && !data.height) return `0 0 ${data.width} 256`
  if (pack === 'logos') return '0 0 256 256'
  if (pack === 'fa6-brands') return '0 0 512 512'
  if (pack === 'cib') return '0 0 32 32'
  if (pack === 'arcticons') return '0 0 48 48'
  return '0 0 24 24'
}

function parseSvgFile(svgText) {
  const vb = svgText.match(/viewBox="([^"]+)"/)
  const viewBox = vb ? vb[1] : '0 0 24 24'
  const inner = svgText
    .replace(/<\?xml[^?]*\?>/g, '')
    .replace(/<svg[^>]*>/, '')
    .replace(/<\/svg>\s*$/i, '')
    .trim()
  return { viewBox, body: inner }
}

