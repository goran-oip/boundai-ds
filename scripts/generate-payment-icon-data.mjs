const SURFACE = {
  klarna: { kind: 'brand', bg: '#feb4c7' },
  sofort: { kind: 'brand', bg: '#f884a1' },
}

const entries = {}
const missing = []

for (const key of Object.keys(MAP).sort()) {
  const spec = MAP[key]
  if ('file' in spec) {
    const svgPath = join(root, 'src/assets/payment-raster', spec.file)
    const parsed = parseSvgFile(readFileSync(svgPath, 'utf8'))
    entries[key] = {
      viewBox: parsed.viewBox,
      body: parsed.body,
      source: 'figma',
    }
    continue
  }
  const pool =
    spec.pack === 'logos'
      ? logos
      : spec.pack === 'simple-icons'
        ? simpleIcons
        : spec.pack === 'cib'
          ? cib
          : spec.pack === 'fa6-brands'
            ? fa6Brands
            : arcticons
  const data = pool[spec.icon]
  if (!data?.body) {
    missing.push(key)
    continue
  }
  entries[key] = {
    viewBox: viewBoxFor(spec.pack, data),
    body: data.body,
    source: 'iconify',
  }
}

if (missing.length) {
  console.error('Missing icon bodies:', missing)
  process.exit(1)
}

const surfaceOut = {}
for (const k of Object.keys(entries)) {
  surfaceOut[k] = SURFACE[k] ?? { kind: 'default' }
}

const out = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/generate-payment-icon-data.mjs — do not edit by hand.
 * Sources: @iconify-json/logos, simple-icons, cib, arcticons; Figma exports in src/assets/payment-raster/.
 */
export const PAYMENT_ICON_ENTRIES = ${JSON.stringify(entries, null, '\t')} as const

export type BdPaymentMethodName = keyof typeof PAYMENT_ICON_ENTRIES

export const PAYMENT_METHOD_SURFACE = ${JSON.stringify(surfaceOut, null, '\t')} as const
`

const target = join(root, 'src/components/icons/payment-icon-data.ts')
writeFileSync(target, out)
console.log('Wrote', target, 'methods:', Object.keys(entries).length)
