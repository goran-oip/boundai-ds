/**
 * Dev: extracts circle-flag SVG bodies for bd-country-icon from @iconify-json/circle-flags.
 * Run: node scripts/generate-country-icon-data.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const iconsPath = join(root, 'node_modules/@iconify-json/circle-flags/icons.json')
const raw = JSON.parse(readFileSync(iconsPath, 'utf8'))
const icons = raw.icons

const bodies = {}
for (const key of Object.keys(icons).sort()) {
  const data = icons[key]
  if (!data?.body || data.hidden) continue
  bodies[key] = data.body
}

const out = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/generate-country-icon-data.mjs — do not edit by hand.
 * Source: @iconify-json/circle-flags (HatScripts, MIT)
 */
export const COUNTRY_ICON_BODIES = ${JSON.stringify(bodies, null, '\t')} as const

export type BdCountryIconName = keyof typeof COUNTRY_ICON_BODIES
`

const target = join(root, 'src/components/icons/country-icon-data.ts')
writeFileSync(target, out)
console.log('Wrote', target, 'keys:', Object.keys(bodies).length)
